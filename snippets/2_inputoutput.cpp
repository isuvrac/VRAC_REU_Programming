#include <iostream>

using namespace std;

int main() {
    /*
    ----------------------------------------
        cout and cin come from the iostream
        library and are used to print to the
        console and get user input
    ----------------------------------------
    */

    // cout
    int x = 5;
    cout << "Hello World!" << endl;
    cout << "x = " << x << endl;

    // cin
    int a;
    cout << "Enter a number: "; // prompt user
    cin >> a; // get user input and store in a
}
